export function getCountryName(id: string, englishName: string): string {
  const locale = getLocale();
  if (locale === 'en') return englishName;
  return countryNamesByLocale[locale]?.[id] ?? englishName;
}

// Translate a FlagItem's name for display
export function flagName(flag: FlagItem): string {
  return getCountryName(flag.id, flag.name);
}

// Build a reverse lookup from English name to id (lazily initialized)
let reverseMap: Map<string, string> | null = null;

function getReverseMap(): Map<string, string> {
  if (!reverseMap) {
    const { countries } = require('./countries');
    reverseMap = new Map((countries as FlagItem[]).map((c) => [c.name, c.id]));
  }
  return reverseMap;
}

// Translate an English country name string (e.g. from options array)
export function translateName(englishName: string): string {
  const locale = getLocale();
  if (locale === 'en') return englishName;
  const id = getReverseMap().get(englishName);
  if (!id) return englishName;
  return countryNamesByLocale[locale]?.[id] ?? englishName;
}
